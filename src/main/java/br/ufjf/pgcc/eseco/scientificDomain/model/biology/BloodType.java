package br.ufjf.pgcc.eseco.scientificDomain.model.biology;

public class BloodType {
    private String type;
    private String rh;

    public BloodType() {
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getRh() {
        return rh;
    }

    public void setRh(String rh) {
        this.rh = rh;
    }


}